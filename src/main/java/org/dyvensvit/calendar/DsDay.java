package org.dyvensvit.calendar;

public class DsDay {
    //TODO: Comments
    private String date;
    private String info;
    private String liturgy;
    private String morning;
    private String night;
    private String hours;
    private String readings;
    private String saints;
    private String quotes;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getLiturgy() {
        return liturgy;
    }

    public void setLiturgy(String liturgy) {
        this.liturgy = liturgy;
    }

    public String getMorning() {
        return morning;
    }

    public void setMorning(String morning) {
        this.morning = morning;
    }

    public String getNight() {
        return night;
    }

    public void setNight(String night) {
        this.night = night;
    }

    public String getHours() {
        return hours;
    }

    public void setHours(String hours) {
        this.hours = hours;
    }

    public String getReadings() {
        return readings;
    }

    public void setReadings(String readings) {
        this.readings = readings;
    }

    public String getSaints() {
        return saints;
    }

    public void setSaints(String saints) {
        this.saints = saints;
    }

    public String getQuotes() {
        return quotes;
    }

    public void setQuotes(String quotes) {
        this.quotes = quotes;
    }
}
